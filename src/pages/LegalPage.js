const normalizeDocument = (raw, fallback) => {
  const safeSections = Array.isArray(raw?.sections) ? raw.sections : fallback.sections;
  return {
    document_name: raw?.document_name || fallback.document_name,
    version: raw?.version || fallback.version,
    last_updated: raw?.last_updated || fallback.last_updated,
    summary: raw?.summary || fallback.summary,
    sections: safeSections.map((section) => ({
      heading: section?.heading || '',
      details: Array.isArray(section?.details) ? section.details : [],
    })),
    contact_email: raw?.contact_email || fallback.contact_email,
  };
};

const LegalPage = ({ onShowAuth, documentType = 'terms' }) => {
  const { user } = useAuth();
  const config = useMemo(() => {
    if (documentType === 'privacy') {
      return {
        key: 'privacy',
        endpoint: '/student/legal/privacy-policy',
      };
    }
    return {
      key: 'terms',
      endpoint: '/student/legal/terms-of-use',
    };
  }, [documentType]);

  const [doc, setDoc] = useState(FALLBACK_DOCS[config.key]);
  const [isLoading, setIsLoading] = useState(true);
  const [errorMessage, setErrorMessage] = useState('');
  const introParagraphs = DOCUMENT_INTROS[config.key] || [];

  useEffect(() => {
    let isActive = true;

    const loadDocument = async () => {
      setIsLoading(true);
      setErrorMessage('');
      try {
        const response = await fetch(`${API_BASE_URL}${config.endpoint}`);
        if (!response.ok) {
          throw new Error(`HTTP_${response.status}`);
        }
        const payload = await response.json();
        if (!isActive) return;
        setDoc(normalizeDocument(payload, FALLBACK_DOCS[config.key]));
      } catch (error) {
        if (!isActive) return;
        setDoc(FALLBACK_DOCS[config.key]);
        setErrorMessage('ไม่สามารถโหลดข้อมูลล่าสุดได้ แสดงข้อมูลพื้นฐานแทน');
      } finally {
        if (isActive) {
          setIsLoading(false);
        }
      }
    };

    loadDocument();
    return () => {
      isActive = false;
    };
  }, [config.endpoint, config.key]);

  return (
    <div className="legal-page">
      <Header user={user} onShowAuth={onShowAuth} activeTab={user ? 'courses' : undefined} />

      <main className="legal-main">
        <article className="legal-document">
          <div className="legal-breadcrumb">
            <Link to="/">หน้าหลัก</Link>
            <span aria-hidden="true">/</span>
            <span>{doc.document_name}</span>
          </div>

          <h1 className="legal-title">{doc.document_name}</h1>
          <p className="legal-updated"><strong>Last updated:</strong> {doc.last_updated}</p>
          <p className="legal-summary">{doc.summary}</p>
          <div className="legal-intro">
            {introParagraphs.map((paragraph) => (
              <p key={paragraph}>{paragraph}</p>
            ))}
          </div>

          <div className="legal-meta">
            <span>Version {doc.version}</span>
          </div>

          {isLoading ? <p className="legal-loading">กำลังโหลดข้อมูล...</p> : null}
          {errorMessage ? <p className="legal-error">{errorMessage}</p> : null}

          <div className="legal-sections">
            {doc.sections.map((section, index) => (
              <section className="legal-section-block" key={section.heading}>
                <h2>{index + 1}. {section.heading}</h2>
                <ul>
                  {section.details.map((detail) => (
                    <li key={detail}>{detail}</li>
                  ))}
                </ul>
              </section>
            ))}
          </div>

          <p className="legal-contact">ติดต่อเพิ่มเติม: {doc.contact_email}</p>
        </article>
      </main>
    </div>
  );
};

export default LegalPage;
